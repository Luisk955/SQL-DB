
--
--CATEGORY
-- 

--CREATE CATEGORY
CREATE PROCEDURE [dbo].[CRE_CATEGORY_PR]
	@ID VARCHAR(255),
	@P_NAME VARCHAR(255)
AS
	INSERT INTO UTILS VALUES(@ID,@P_NAME);
GO

-- DELETE CATEGORY
CREATE PROCEDURE [dbo].[DEL_CATEGORY_PR]
	@P_ID VARCHAR(255)
AS
	DELETE FROM UTILS WHERE ID=@P_ID;
GO

--RETRIVE ALL CATEGORY
CREATE PROCEDURE [dbo].[RET_ALL_CATEGORY_PR]
AS
	SELECT * FROM UTILS;
GO

--RETRIEVE CATEGORY BY ID
CREATE PROCEDURE [dbo].[RET_CATEGORY_PR]
	@P_ID VARCHAR(255)
AS
	SELECT * FROM [dbo].Utils where ID=@P_ID;
GO

--UPDATE CATEGORY
CREATE PROCEDURE [dbo].[UPD_CATEGORY_PR]
	@P_ID VARCHAR(255),
	@P_NAME VARCHAR(255)
AS
	UPDATE [dbo].[UTILS] SET ID=@P_ID, NAME=@P_NAME  WHERE ID=@P_ID;
GO

--
