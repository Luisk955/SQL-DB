-- 

-- CREATE ERROR
CREATE PROCEDURE [dbo].[CRE_ERROR_PR]
	@P_ID int,
	@P_NAME nvarchar(25)
AS
	INSERT INTO ERROR VALUES(@P_ID,@P_NAME);
GO

-- RETRIEVE ERROR BY ID
CREATE PROCEDURE [dbo].[RET_ERROR_PR]
	@P_ID varchar(50)
AS
	SELECT * FROM [dbo].Error where ID=@P_ID;